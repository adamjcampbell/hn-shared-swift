package com.example.appcore.state

import androidx.compose.runtime.Composable
import androidx.compose.runtime.getValue
import androidx.compose.runtime.mutableStateOf
import androidx.compose.runtime.setValue
import com.example.appcore.bridge.AppCoreAndroid
import com.example.appcore.bridge.SnapshotSink
import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable
import kotlinx.serialization.json.Json

@Serializable
data class Story(
    val id: String,
    val title: String,
    val author: String,
    val points: Int,
    val commentCount: Int,
    val url: String? = null,
    val createdAt: String,
)

@Serializable
data class AppState(
    val stories: List<Story> = emptyList(),
    val read: Set<String> = emptySet(),
    val searchQuery: String = "",
    val isLoading: Boolean = false,
    val lastRefreshedAt: String? = null,
    val loadError: String? = null,
)

/**
 * Mirrors the Swift `AppEvent` enum. Wire shape is `{"type":"...", ...}`,
 * matching `AppCore/Sources/AppCore/AppEvent.swift`'s `@Codable` +
 * `@CodedAt("type")` MetaCodable annotations.
 */
@Serializable
sealed class AppEvent {
    @Serializable
    @SerialName("toggleRead")
    data class ToggleRead(val id: String) : AppEvent()

    @Serializable
    @SerialName("refresh")
    data object Refresh : AppEvent()

    @Serializable
    @SerialName("setSearchQuery")
    data class SetSearchQuery(val value: String) : AppEvent()
}

/**
 * Process-wide holder for the model snapshot delivered from Swift.
 *
 * The Java surface is generated by `swift-java jextract --mode=jni` from
 * the Swift sources in `AppCoreAndroid`. `AppCoreAndroid` is the name of
 * the generated Java module class (named after the Swift module) and
 * exposes `appcoreCreate / appcoreDispatch / appcoreDestroy` as static
 * methods. All mutations are funnelled through `appcoreDispatch` with a
 * JSON-encoded `AppEvent`; adding a new mutation case in Swift is the
 * only thing required to expose a new action here.
 *
 * `SnapshotSink` is a Java interface jextract generated from the Swift
 * `protocol SnapshotSink` (because `enableJavaCallbacks: true` in
 * `swift-java.config`). We implement it directly on this object.
 *
 * **Why a singleton:** there is one `AppModel` per process on the Swift
 * side too. We initialise once from `AppCoreApplication.onCreate` and
 * leave it running for the process lifetime; the Compose tree just reads
 * `state`.
 *
 * **Loading state:** the snapshot's `isLoading` and `loadError` come
 * straight from Swift — no Kotlin-side timer. Pull-to-refresh's spinner
 * just observes `state.isLoading`.
 */
object AppModelHolder : SnapshotSink {
    var state by mutableStateOf<AppState?>(null)
        private set

    private val json = Json {
        classDiscriminator = "type"
        ignoreUnknownKeys = true
    }

    fun start() {
        AppCoreAndroid.appcoreCreate(this)
    }

    /** Called from Swift via JNI on every Observations transaction. */
    override fun deliver(snapshotJSON: String) {
        state = json.decodeFromString<AppState>(snapshotJSON)
    }

    fun dispatch(event: AppEvent) {
        AppCoreAndroid.appcoreDispatch(json.encodeToString(AppEvent.serializer(), event))
    }
}

@Composable
fun rememberAppModel(): AppModelHolder = AppModelHolder
